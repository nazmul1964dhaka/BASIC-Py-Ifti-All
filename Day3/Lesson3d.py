#String functions
############

s='The quick brown fox jump over the LAZY dog'
print(s)
print(s.capitalize())  #Only 1st character Capital
print(s.title())  #all ist  character  of word is Capital
print(s.upper())  #all are Capital character
print(s.lower()) #all are small character
print(s.swapcase()) #all are toggle character
print(s.split()) #convert to list